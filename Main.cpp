#include <iostream>

using namespace std;

int main()
{
	int Number1 = 20;
	int Number2 = 2;

	cout << (Number1 + Number2) << endl;
	cout << (Number1 - Number2) << endl;
	cout << (Number1 * Number2) << endl;
	cout << (Number1 / Number2) << endl;

	float Number3 = 10.5f;
	float Number4 = 2.0f;

	cout << (Number3 + Number4) << endl;
	cout << (Number3 - Number4) << endl;
	cout << (Number3 * Number4) << endl;
	cout << (Number3 / Number4) << endl;

	return 0;
}